function CalcCircumferArea(radius)
{
    var pi=3.14
    var Circumference=(2*pi*radius)
    console.log("The Circumference of a Circle is "+Circumference+"")
    var area=(pi*(radius*radius))
    console.log("The area of a Circle is "+area+"")
}
